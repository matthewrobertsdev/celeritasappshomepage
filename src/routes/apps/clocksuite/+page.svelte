<h2 class="left-h2">Clock</h2>
  <ul class="feature-list">
  <li>—Completely resizable, just by dragging its edges, anywhere from small to full screen.</li>
  <li>—Can show or hide seconds.</li>
  <li>—Can be digital or analog.</li>
  <li>—Can show or hide the date and the day of the week.</li>
  <li>—Can have date be written out or be numerical.</li>
  <li>—Clock can float on top or go behind other windows.</li>
</ul>

<h2 class="left-h2">Colors</h2>
<ul class="feature-list">
<li>—Comes with 13 built-in colors and a choice to choose a custom color, all available with dark and light mode versions. Colors are a system dark or light color, black, gray, white, red, orange, yellow, green, blue, indigo, purple, pink, brown, or a custom color.</li>
<li>—Works in system-wide dark mode and light mode. The app’s colors will automatically change when dark mode or light mode changes.</li>
<li>—Unified colors for the entire app with a single click.</li>
<li>—Color choice can be the background color or the foreground color.</li>
<li>—When the color choice is the foreground, background can be dark gray or the active window can be translucent. This can be set in preferences.  Windows are translucent only when they are the active window (such as when they are the last window you clicked on).</li>
</ul>

<h2 class="left-h2">Gradients</h2>
<ul class="feature-list">
<li>—The background colors for Clock Suite windows now can have a gradient from dark to light. (Or not.  It's your choice.)</li>
</ul>

<h2 class="left-h2">Night Mode</h2>
<ul class="feature-list">
<li>—Night mode feature allows you to have dark text even in dark mode if you want the app to be easy on tired eyes and tired people.</li>
</ul>

<h2 class="left-h2">Alarms and Timers</h2>
<ul class="feature-list">
<li>—Up to 24 Alarms.</li>
<li>—3 Countdown Timers that can be set for up to 23:59:59 hours each, or as short as 1 second.</li>
<li>—Timers can display as hours/minutes/seconds or as total seconds remaining.</li>
<li>—Timers can float on top or go behind other windows.</li>
<li>—Please note: as your computer will not sleep automatically when an alarm or timer is active, you may need to have your computer connected to power when setting alarms or timers for long periods of time, since it will use more energy when it is not sleeping, and you do not want your computer to run out of power and shut off because then your alarm or timer will not go off.</li>
</ul>

<h2 class="left-h2">Alerts and Songs</h2>
<ul class="feature-list">
<li>—Play songs or alerts when an alarm or timer goes off. (Please note—does not work with songs from the Apple Music Subscription. You must own a copy of the song you wish to play.)</li>
<li>—Can have a playlist of songs from which to choose a song to play when an alarm or timer goes off.</li>
<li>—Active alarms and timers will block your computer from sleeping unless you tell it to sleep, so you don't have to worry that your alarm or timer will not go off.</li>
</ul>

<h2 class="left-h2">Stopwatch</h2>
<ul class="feature-list">
<li>—Keep track of time with the stopwatch.</li>
<li>—Click the lap button to time an individual lap.</li>
<li>—Keep track of all your laps and add notes to help you remember what you were measuring.</li>
<li>—Export laps to csv file that has lap numbers, lap times, and lap notes.</li>
<li>—Choose between seconds precision and tenths of seconds precision.</li>
<li>—Stopwatch can float on top or go behind other windows.</li>
</ul>

<h2 class="left-h2">Dock Clock</h2>
<ul class="feature-list">
<li>—Runs when your app is in use or just open.</li>
<li>—Can show or hide seconds.</li>
<li>—Can be digital or analog.</li>
</ul>

<h2 class="left-h2">Miscellaneous</h2>
<ul class="feature-list">
<li>—Can use 24-hour mode or AM/PM mode.  You can choose this in the app’s preferences.</li>
</ul>

