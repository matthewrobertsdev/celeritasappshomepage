<h2 class="left-h2">How do I enable translucency for the active Clock Suite window?</h2>
<p class="body-p">You must open Clock Suite Settings under the Clock Suite menu and select "Translucent", under "Color For Foreground Background". You also must have selected, under the Colors menu, "Color for Foreground" to have an active window be translucent. Only a window that is active will be translucent (such as the last window you clicked on). If it is no longer the active window, it will no longer be translucent. If you click on it again, it will become translucent again. To return to a dark gray background for when the chosen color is the foreground, go to Clock Suite Settings under the Clock Suite menu and select "Dark Gray", under "Color For Foreground Background". Once again, "Color for Foreground" must be selected to see the dark gray background.</p>
<h2 class="left-h2">How do I switch between AM/PM mode and using a 24 hour clock?</h2>
<p class="body-p">You must open Clock Suite Stetings under the Clock Suite menu and select or deselect the "Use 24 Hour Clock" checkbox.</p>
